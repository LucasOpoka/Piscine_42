#include <unistd.h>

void	f2(char *s)
{
	if (s[3] > s[0] || (s[3] == s[0] && s[4] > s[1]))
	{
		write (1, s, 5);
		if (!(s[0] == '9' && s[1] == '8'
				&& s[3] == '9' && s[4] == '9'))
			write (1, ", ", 2);
	}
}

void	ft_print_comb2(void)
{
	char	s[5];

	s[0] = '0';
	s[2] = ' ';
	while (s[0] <= '9')
	{
		s[1] = '0';
		while (s[1] <= '9')
		{
			s[3] = '0';
			while (s[3] <= '9')
			{
				s[4] = '0';
				while (s[4] <= '9')
				{
					f2(s);
					s[4]++;
				}
				s[3]++;
			}
			s[1]++;
		}
		s[0]++;
	}
}
